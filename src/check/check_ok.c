int	dir_ok(char *dir)
{
	if (strlen(dir) == 1 
		&& (dir[0] == 'N' 
			|| dir[0] == 'S' 
			|| dir[0] == 'E' 
			|| dir[0] == 'W'))
		return (1);
	return (0);
}